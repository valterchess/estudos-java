package com.princeton.firstpart.cap2.p2;

public class MetodosDaMath {
    public static void main(String[] args) {
        double getAbs = Math.abs(5.3);
        double getMax = Math.max(3,9);
        double getMin = Math.min(4,2);
        double getSin = Math.sin(6.8);
        double getCos = Math.cos(Math.PI);
        double getTan = Math.tan(Math.sqrt(2));
        double getToRadians = Math.toRadians(27);
        double getToDegrees = Math.toDegrees(123);
        double getExp = Math.exp(3);
        double getLog = Math.log(2);
        double getPow = Math.pow(3,2);
        double getRound = Math.round(4);
        double getRandom = Math.random();
        double getSqrt = Math.sqrt(9);
        double getE = Math.E;
        double getPi = Math.PI;

        System.out.println(getAbs);
        System.out.println(getMax);
        System.out.println(getMin);
        System.out.println(getSin);
        System.out.println(getCos);
        System.out.println(getTan);
        System.out.println(getToRadians);
        System.out.println(getToDegrees);
        System.out.println(getExp);
        System.out.println(getLog);
        System.out.println(getPow);
        System.out.println(getRound);
        System.out.println(getRandom);
        System.out.println(getSqrt);
        System.out.println(getE);
        System.out.println(getPi);
    }
}
